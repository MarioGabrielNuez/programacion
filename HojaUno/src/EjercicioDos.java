public class EjercicioDos {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		byte N1 = 7;
		byte N2 = 8;
		
		System.out.println(N1 + " + " + N2 + " = " + (N1 + N2));
		System.out.println(N1 + " - " + N2 + " = " + (N1 - N2));
		System.out.println(N1 + " * " + N2 + " = " + (N1 * N2));
		System.out.println(N1 + " / " + N2 + " = " + (N1 / N2));
		System.out.println(N1 + " MOD " + N2 + " = " + (N1 % N2));
	}

}
